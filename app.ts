//Data Types

//String
let message:string = "Ghazal";
console.log(message);

//Number
let myNumber:number = 5;
console.log(myNumber);

//Boolean
let sunnyDay:boolean = true;
console.log(sunnyDay);

//Null
let nullType:null = null;
console.log(nullType);

//undefined
let undefinedType:undefined = undefined;
console.log(undefinedType);

//Any
let anyMessage:any = "Let's go";
console.log(anyMessage);

//Bigint
let bigNumber:bigint = 150000n;
console.log(bigNumber);

//Symbol
let mySymbol:symbol = Symbol("hi");
console.log(mySymbol);





